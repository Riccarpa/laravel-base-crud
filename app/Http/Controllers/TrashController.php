     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $comic = Comic::withTrashed()->find($id);
         $comic->restore();
         return redirect()->route('comics.index')->with('restore', $comic->title);
        
    }

    /**
